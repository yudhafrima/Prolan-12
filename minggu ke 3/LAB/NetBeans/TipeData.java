


public class TipeData {

	public static void main(String[] args) {
		//tipe data objek
		String nama = "Belajar Java";
		System.out.println(nama.length());
		
		// tipe data byte
		byte maxByte = 127;
		byte minByte = -128;
		byte defaultByte = 0;
		System.out.println(defaultByte);
		
		// tipe data short
		short maxShort = 32767;
		short minShort = -128;
		short defaultShort = 0;
		System.out.println(defaultShort);
		
		// tipe data int
		int maxInt = 2147483647;
		int minInt = -2147483648;
		int defaultInt = 0;
		System.out.println(defaultInt);
		
		// tipe data long
		long maxLong = 9223372036854775807L;
		long minLong = -9223372036854775808L;
		long defaultLong = 0L;
		System.out.println(defaultLong);
		
		// tipe data float
		float maxFloat = 3.40282347999999999999999999999999999999f;
		float minFloat = -3.40282349999999999999999999999999999999f;
		float defaultFloat = 0.0f;
		System.out.println(maxFloat);
		System.out.println(minFloat);
		
		// tipe data double
		double randomDouble = 1.797693134862315702d;
		double randomDoublel = -1.797693134862315602d;
		double defaultDouble = 0.0;
		System.out.println(randomDouble);
		System.out.println(randomDoublel);
		
		// tipe data boolean
		boolean menikah = false;
		System.out.println(menikah);
		
		// tipe data char
		char A = 'A';
		System.out.println(A);
        }
}   
		